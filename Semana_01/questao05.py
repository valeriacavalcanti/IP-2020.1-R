n1, n2 = input('Informe dois números: ').split()
n1, n2 = int(n1), int(n2)

soma = n1 + n2
diferenca = n1 - n2
produto = n1 * n2
quociente = n1 // n2
resto = n1 % n2
potencia = n1 ** n2

print('Soma =', soma)
print('Diferença =', diferenca)
print('Produto =', produto)
print('Quociente = ', quociente)
print('Resto =', resto)
print('Potência =', potencia)

